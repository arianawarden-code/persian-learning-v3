export interface Module {
  id: string | number
  title: string
  description: string
  level: "alphabet" | "beginner" | "intermediate" | "advanced"
  topics: string[]
}

export interface VocabularyWord {
  persian: string
  transliteration: string
  english: string
  example: string
  exampleTranslation: string
}

export interface ReadingExercise {
  id: number
  title: string
  text: string
  textTransliteration?: string
  textTranslation: string
  questions: {
    question: string
    options: string[]
    correctAnswer: number
  }[]
}

export interface GrammarExercise {
  id: number
  type: "letter-position" | "letter-identification" | "word-analysis"
  instruction: string
  // Teaching content shown before the quiz
  teachingContent?: {
    title: string
    explanation: string
    examples: {
      label: string
      persian: string
      description: string
    }[]
  }
  question: string
  options: string[]
  correctAnswer: number
  explanation?: string
}

// Define WritingExercise interface
export interface WritingExercise {
  id: number
  type: "build-word" | "sentence"
  instruction: string
  targetWord?: string
  targetWordTransliteration?: string
  availableLetters?: string[]
  prompt?: string
}

export interface ModuleContent {
  moduleId: string | number
  vocabulary: VocabularyWord[]
  reading: ReadingExercise[]
  writing: WritingExercise[]
  grammar?: GrammarExercise[]
}

